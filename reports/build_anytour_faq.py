"""Сборка per-tenant FAQ для AnyTour (Павел Пятков) из глобального faq.md.

Берём faq.md, оставляем разделы 1–15 (общие знания о турах) с заменой
MGP-данных на AnyTour, и полностью переписываем §16–17 + контакты под AnyTour:
5 офисов, телефон 8 (800) 700-29-15, режим работы как у офисов МГП.

Запуск:
    python3 reports/build_anytour_faq.py
Результат: reports/anytour_faq.md
"""
import os
import re

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
SRC = os.path.join(ROOT, "faq.md")
OUT = os.path.join(ROOT, "reports", "anytour_faq.md")

PHONE = "8 (800) 700-29-15"
SITE = "anytour.online"

with open(SRC, encoding="utf-8") as f:
    text = f.read()

# Отрезаем всё начиная с §16 — этот хвост (компания/офисы/контакты МГП)
# заменим собственным блоком AnyTour.
cut = text.index("## 16.")
generic = text[:cut].rstrip()

# ── Замены MGP → AnyTour в общей части (разделы 1–15) ──
# Порядок важен: сначала длинные токены (email), потом короткие (домен).
generic = generic.replace("online@mgp.ru", SITE)
generic = generic.replace("mgp.ru", SITE)
generic = generic.replace("+7 (499) 685-25-57", PHONE)
generic = generic.replace("Магазин Горящих Путёвок", "AnyTour")
generic = generic.replace("Магазин горящих путёвок", "AnyTour")
generic = generic.replace("{{MANAGER_PHONE}}", PHONE)
generic = generic.replace("{{COMPANY_NAME}}", "AnyTour")
# Заголовок
generic = re.sub(r"^# FAQ —.*$", "# FAQ — База знаний AI-ассистента «AnyTour»", generic, count=1, flags=re.M)

anytour_tail = f"""

---

## 16. О КОМПАНИИ И КОНТАКТЫ

**В: Кто такие «AnyTour»?**
О: Турагентство AnyTour. Подбираем и продаём туры от ведущих туроператоров. Все туры — с договором и документальным подтверждением. Сайт: {SITE}.

**В: В чём преимущество бронирования через агентство?**
О: Экономия времени (туры от всех операторов в одном месте), профессиональная консультация и поддержка в случае форс-мажора. Цена тура — такая же, как напрямую у туроператора.

**В: Если ответ от отеля придёт ночью, мы узнаем?**
О: Онлайн-поддержка работает с 09:00 до 21:00 по московскому времени. Заявки с вылетом на следующий день обрабатываются в первую очередь.

**В: Как с вами связаться?**
О: Единый телефон: {PHONE}. Сайт: {SITE}.

**В: Какие услуги входят в стоимость тура?**
О: Перелёт, проживание, трансфер и медицинская страховка. Конкретный перечень указан на странице бронирования. Дополнительные услуги можно указать при оформлении в комментариях.

---

## 17. ОФИСЫ AnyTour

**Режим работы офисов:** Пн–Пт 10:00–19:00, Сб 10:00–17:00, Вс — выходной. В праздничные дни расписание может меняться — рекомендуем уточнить по телефону {PHONE}.

**Единый телефон для всех офисов:** {PHONE}

**В: Где находятся ваши офисы?**
О: У AnyTour 5 офисов в России:
- **Москва** — ул. Бутырская, 86Б, 2 этаж
- **Санкт-Петербург** — ул. Белы Куна, 3, 3-й этаж, ТЦ «Международный»
- **Калининград** — ул. Нарвская, 10А
- **Чебоксары** — проспект Максима Горького, 26
- **Красноярск** — ул. Караульная, 43/3

Назовите удобный город — подскажу адрес ближайшего офиса. По любым вопросам можно позвонить по единому номеру {PHONE}.

**В: Какой режим работы у офисов?**
О: Понедельник–пятница с 10:00 до 19:00, суббота с 10:00 до 17:00, воскресенье — выходной. В праздничные дни расписание может отличаться — лучше уточнить по телефону {PHONE}.

**В: Есть ли единый телефон?**
О: Да — {PHONE}. Менеджер ответит на любые вопросы и поможет с подбором и оформлением тура.

---

### Единые контакты

- **Единый телефон:** {PHONE}
- **Сайт:** {SITE}

> Если в вашем городе нет офиса — позвоните по {PHONE} или оставьте заявку на сайте {SITE}: оформим тур онлайн с доставкой документов электронно.
"""

result = generic + anytour_tail

with open(OUT, "w", encoding="utf-8") as f:
    f.write(result)

# Санити-проверка: не осталось ли MGP-данных
leftovers = []
for bad in ("mgp.ru", "online@mgp", "499) 685", "Магазин Горящих", "Магазин горящих",
            "{{MANAGER_PHONE}}", "{{COMPANY_NAME}}", "get_offices", "16 офисов", "Подмосковье"):
    if bad in result:
        leftovers.append(bad)

print(f"written {OUT}  ({len(result)} chars)")
print(f"PHONE occurrences: {result.count(PHONE)}")
print(f"leftover MGP markers: {leftovers if leftovers else 'NONE ✅'}")
