"""Lead-catcher — пер-тенант режим «ловца лидов» (старт: только AnyTour).

Модуль СОЗНАТЕЛЬНО без Flask/DB зависимостей: только `config.settings` (для
allow-list) + stdlib. Тестируется изолированно — как `manager_handoff.py`.

Инвариант безопасности: пока `assistant_id` НЕ в `lead_catcher_assistant_ids`,
`is_lead_catcher()` возвращает False, и весь остальной код режима становится
no-op. Базовый `system_prompt.md` НЕ модифицируется — lead-catcher-политика
ПРЕПЕНДится только для включённых ассистентов, поэтому другие тенанты получают
ровно тот же промпт, что и раньше (байт-в-байт).

Что делает режим (для включённого ассистента):
  • intake без «анкеты»: собираем минимум (направление, город вылета, даты,
    состав) гибко; звёзды/питание НЕ спрашиваем — применяем умные дефолты;
  • кодовый гейт каскада не блокирует поиск из-за отсутствия QC
    (см. yandex_handler._check_cascade_slots(lead_catcher=True));
  • умные дефолты по стране (зеркало system_prompt.md §3.7.1);
  • человеческая рекомендация в карточке (факты из данных + правила курортов).
"""
from __future__ import annotations

