from typing import Dict, List, Optional

# Префикс кода питания в meal_description Tourvisor: "BB - ", "AI -", "UAI - "…
_MEAL_CODE_PREFIX = re.compile(r'^[A-Za-z]{2,4}\+?\s*[-–—]\s*')


def _split_csv(raw: str) -> List[str]:
    return [item.strip() for item in (raw or "").split(",") if item.strip()]


# ──────────────────────────────────────────────────────────────────────────────
# LEAD FILTER — чистая логика: доводить ли заявку до менеджеров.
# Запрос Павла (Anytour) 2026-06-20: не отправлять менеджерам заявки по РФ/Абхазии
# и/или дешевле порога суммы тура. Здесь — только решение (без БД/сети); вызов и
# гейт (widget_config.lead_filter, LEAD_FILTER_ENABLED) — в yandex_handler.
# ──────────────────────────────────────────────────────────────────────────────

# Названия стран → код TourVisor (бэкап-детекция: в карточке брони страна
# приходит строкой, а не кодом). РФ=47, Абхазия=46.
_LEAD_FILTER_NAME2CODE = {
    "россия": 47, "russia": 47, "рф": 47,
    "абхазия": 46, "abkhazia": 46,
}


def lead_suppression_decision(
    lead_filter: Optional[Dict],
    country_code: Optional[int],
    tour_price: Optional[int],
    dest_country_name: str = "",
) -> Optional[str]:
    """Причина НЕ доводить заявку до менеджеров, либо None.

      • 'blocked_country' — направление диалога в block_countries (РФ/Абхазия);
      • 'below_min_price' — известная цена тура ниже min_tour_price.

    Любой пустой/некорректный конфиг → None (фича выключена для тенанта).
    Цена неизвестна (None) → по порогу НЕ подавляем (не теряем потенциальный лид).
    """
    if not isinstance(lead_filter, dict) or not lead_filter:
        return None
    try:
        block_set = {int(x) for x in (lead_filter.get("block_countries") or [])}
    except (TypeError, ValueError):
        block_set = set()
    if block_set:
        if country_code in block_set:
            return "blocked_country"
        code = _LEAD_FILTER_NAME2CODE.get((dest_country_name or "").strip().lower())
        if code in block_set:
            return "blocked_country"
    raw_floor = lead_filter.get("min_tour_price")
    try:
        floor = int(raw_floor) if raw_floor else None
    except (TypeError, ValueError):
        floor = None
    if floor and tour_price is not None and tour_price < floor:
        return "below_min_price"
    return None


def is_lead_catcher(assistant_id: Optional[str]) -> bool:
    """Активен ли режим «ловца лидов» для данного assistant_id.

    Любая осечка → False (режим инертен). Никаких исключений наружу.
    """
    if not assistant_id:
        return False
    try:
        from config import settings
    except Exception:
        try:
            from backend.config import settings  # type: ignore
        except Exception:
            return False
    allow = set(_split_csv(getattr(settings, "lead_catcher_assistant_ids", "")))
    return str(assistant_id) in allow


# ── Архетипы направлений + умные дефолты по стране ──
# Архетип определяет (а) дефолты поиска и (б) подачу ценовой лестницы (П.2):
#   • beach_ai      — пляж «всё включено», лестница 4★/5★ AI;
#   • beach_bb      — пляж «завтраки/премиум», лестница 4★/5★ BB;
#   • exotic        — премиум-экзотика, дефолт 5★ (Мальдивы/Маврикий/Сейшелы);
#   • excursion     — экскурсионные, БЕЗ звёздной лестницы (ось — дни/программа);
#   • cis           — Россия/СНГ, без фильтра питания;
#   • generic       — прочее, золотая середина.
# meal: 7 = AI (всё включено), 3 = BB (только завтрак).
# Классификация — по реальному трафику AnyTour (Павел), согласовано 2026-06-24.

# A. Пляж «всё включено».
_ARCH_BEACH_AI = frozenset({4, 1, 5, 10, 11})            # Турция, Египет, Тунис, Куба, Доминикана
# Премиум-экзотика — дефолт 5★ (острова-курорты «отель = отдых»).
_ARCH_EXOTIC_PREMIUM = frozenset({8, 27, 28})            # Мальдивы, Маврикий, Сейшелы
# B. Пляж «завтраки/премиум» — дефолт 4★ (подняли с 3★ для роста чека).
_ARCH_BEACH_BB = frozenset({
    9, 7,                # ОАЭ, Индонезия/Бали
    2, 16, 12, 26, 36,   # Таиланд, Вьетнам, Шри-Ланка, Филиппины, Малайзия
    3,                   # Индия (Гоа доминирует в турпакетах)
    6, 15, 21, 22, 20,   # Греция, Кипр, Черногория, Хорватия, Болгария
    41, 51,              # Танзания/Занзибар, Кения
})
# C. Экскурсионные — звёздная лестница НЕ главная (ось — дни/уровень/индивид.).
_ARCH_EXCURSION = frozenset({
    13, 70, 49, 25,      # Китай, Южная Корея, Япония, Сингапур
    24, 32, 14, 44, 31,  # Италия, Франция, Испания, Великобритания, Австрия
})
# D. Россия/СНГ: очень разнообразно — НЕ фильтруем по питанию.
_RUSSIA_CIS_COUNTRIES = frozenset({47, 46, 54, 53, 55, 57, 56, 78, 73, 79, 60})


def destination_archetype(country: Optional[int]) -> str:
    """Архетип направления по коду страны TourVisor.

    Значения: 'beach_ai' | 'beach_bb' | 'exotic' | 'excursion' | 'cis' |
    'generic'. Неизвестно/0 → 'generic'. Никаких исключений наружу.
    """
    try:
        c = int(country)
    except (TypeError, ValueError):
        return "generic"
    if c <= 0:
        return "generic"
    if c in _ARCH_EXOTIC_PREMIUM:
        return "exotic"
    if c in _ARCH_BEACH_AI:
        return "beach_ai"
    if c in _ARCH_BEACH_BB:
        return "beach_bb"
    if c in _ARCH_EXCURSION:
        return "excursion"
    if c in _RUSSIA_CIS_COUNTRIES:
        return "cis"
    return "generic"


def smart_qc_defaults(country: Optional[int]) -> Dict[str, int]:
    """Рекомендованные stars/meal по стране (когда клиент НЕ указал QC).

    Возвращает словарь из части ключей: stars, starsbetter, meal, mealbetter.
    Для России/СНГ ключи meal/mealbetter отсутствуют (не фильтруем по питанию).
    Тянем средний чек: пляж-завтраки и экзотика идут не от низа (4★/5★).
    """
    arch = destination_archetype(country)
    if arch == "generic":
        # generic покрывает и country<=0/неизвестно — там вернём пусто/середину.
        try:
            c = int(country)
        except (TypeError, ValueError):
            c = 0
        if c <= 0:
            return {}
        return {"stars": 3, "starsbetter": 1, "meal": 3, "mealbetter": 1}
    if arch == "exotic":
        # Премиум-экзотика: 5★ — это и есть «уровень направления».
        return {"stars": 5, "starsbetter": 0, "meal": 3, "mealbetter": 1}
    if arch == "beach_ai":
        return {"stars": 4, "starsbetter": 1, "meal": 7, "mealbetter": 0}
    if arch == "beach_bb":
        return {"stars": 4, "starsbetter": 1, "meal": 3, "mealbetter": 1}
    if arch == "excursion":
        # Экскурсионные: звёздность не главное — не раздуваем (ось — программа).
        return {"stars": 3, "starsbetter": 1, "meal": 3, "mealbetter": 1}
    if arch == "cis":
        return {"stars": 3, "starsbetter": 1}
    return {"stars": 3, "starsbetter": 1, "meal": 3, "mealbetter": 1}


def apply_smart_defaults(args: Dict) -> List[str]:
    """Заполнить отсутствующие stars/meal умными дефолтами по country.

    Безопасно:
      • НИКОГДА не перезаписывает явно переданные клиентом параметры
        (ставим только когда ключ отсутствует или None);
      • при `hotels` (бренд-поиск) НЕ трогает stars/meal вовсе (звёздность из
        каталога — зеркалит safety-net в search_tours);
      • при country=0/неизвестно — ничего не делает.
    Возвращает список применённых ключей (для логирования).
    """
    if args.get("hotels"):
        return []
    defaults = smart_qc_defaults(args.get("country"))
    if not defaults:
        return []
    applied: List[str] = []
    for key, val in defaults.items():
        if args.get(key) is None:
            args[key] = val
            applied.append(f"{key}={val}")
    return applied


