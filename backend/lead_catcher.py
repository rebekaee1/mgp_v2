def resort_note(resort_name: Optional[str]) -> str:
    """Короткая характеристика курорта по названию региона (или '')."""
    name = (resort_name or "").strip().lower()
    if not name:
        return ""
    for key, note in _RESORT_RULES.items():
        if key in name:
            return f"{resort_name.strip()}: {note}"
    return ""


def _card_head(card: Dict) -> str:
    """Факт-«шапка» карточки строго из её полей: «5★ · всё включено» (или '')."""
    head_parts: List[str] = []
    try:
        stars = int(card.get("hotel_stars") or 0)
    except (TypeError, ValueError):
        stars = 0
    if stars > 0:
        head_parts.append(f"{stars}★")
    meal = (card.get("meal_description") or "").strip()
    meal = _MEAL_CODE_PREFIX.sub("", meal).strip()   # "BB - Только завтрак" → "Только завтрак"
    if meal:
        head_parts.append(meal.lower())
    return " · ".join(head_parts)


def build_recommendation(card: Dict) -> str:
    """Детерминированная человеческая строка-рекомендация СТРОГО из фактов карточки.

    Пример: «5★ · всё включено — Белек: песчаные пляжи, премиальные отели, тихо
    — для семьи». Если фактов мало — вернёт короткую строку или ''.
    Никаких выдуманных деталей: только звёзды, питание (как в карточке) и
    нейтральная характеристика курорта из таблицы правил.
    """
    head = _card_head(card)
    note = resort_note(card.get("resort") or card.get("region"))
    if head and note:
        return f"{head} — {note}"
    return note or head


def assign_recommendations(cards: List[Dict]) -> None:
    """Проставить card['recommendation'] для СПИСКА карточек с дедупом курортной
    ремарки.

    Проблема, которую решаем: у нескольких отелей ОДНОГО курорта raw-функция
    build_recommendation давала одинаковый «хвост» (описание курорта), и на
    карточках появлялись идентичные 💡. Здесь описание курорта показываем
    ОДИН раз — у первой карточки этого курорта; у последующих оставляем только
    факты (звёзды·питание) + НАЗВАНИЕ курорта без повторного описания.

    Мутирует карточки на месте. Инертно для не-lead-catcher (вызывается только
    из lead-catcher ветки хендлера).
    """
    seen_resorts: set = set()
    for c in cards or []:
        if not isinstance(c, dict):
            continue
        head = _card_head(c)
        note_full = resort_note(c.get("resort") or c.get("region"))
        if note_full:
            # note_full == «Курорт: описание»
            label = note_full.split(":", 1)[0].strip()
            key = label.lower()
            if key in seen_resorts:
                rec = f"{head} — {label}" if head else label
            else:
                seen_resorts.add(key)
                rec = f"{head} — {note_full}" if head else note_full
        else:
            rec = head
        if rec:
            c["recommendation"] = rec


def _card_price(card: Dict) -> int:
    """Цена карточки как int (для пометки «самый доступный»). 0 если нет."""
    try:
        return int(card.get("price") or 0)
    except (TypeError, ValueError):
        return 0


def build_cards_digest(cards: List[Dict]) -> str:
    """Компактная факт-выжимка по показанным карточкам — ДЛЯ МОДЕЛИ (не клиенту).

    Назначение: дать LLM ровно те факты, что видит клиент под карточками
    (курорт + черта), чтобы ассистент мог осознанно выделить 1-2 варианта под
    запрос — БЕЗ выдумок. Нумерация совпадает с карточками у клиента.

    Пример строки: «1. Белек, 5★, всё включено — сосны и песчаные пологие
    пляжи… (самый доступный по цене)». Возвращает '' если карточек нет.
    """
    cards = [c for c in (cards or []) if isinstance(c, dict)]
    if not cards:
        return ""
    prices = [(_card_price(c), i) for i, c in enumerate(cards)]
    cheapest_idx = min((p for p in prices if p[0] > 0), default=(0, -1))[1]
    lines: List[str] = []
    seen_resorts: set = set()   # дедуп описания курорта (одно описание на курорт)
    for i, c in enumerate(cards, 1):
        resort = (c.get("resort") or c.get("region") or "").strip()
        try:
            stars = int(c.get("hotel_stars") or 0)
        except (TypeError, ValueError):
            stars = 0
        meal = _MEAL_CODE_PREFIX.sub("", (c.get("meal_description") or "").strip()).strip()
        note_full = resort_note(resort)
        # resort_note возвращает «Курорт: текст» — берём только характеристику.
        note = note_full.split(":", 1)[1].strip() if ":" in note_full else ""
        head_bits: List[str] = []
        if resort:
            head_bits.append(resort)
        if stars > 0:
            head_bits.append(f"{stars}★")
        if meal:
            head_bits.append(meal.lower())
        head = ", ".join(head_bits)
        line = f"{i}. {head}" if head else f"{i}."
        # описание курорта показываем один раз — у первой карточки этого курорта
        _rk = resort.lower()
        if note and _rk not in seen_resorts:
            line += f" — {note}"
            seen_resorts.add(_rk)
        if (i - 1) == cheapest_idx and len(cards) > 1:
            line += " (самый доступный по цене)"
        lines.append(line)
    return "\n".join(lines)


