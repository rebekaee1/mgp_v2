-- Включение lead-filter ТОЛЬКО для Anytour (Павел Пятков).
-- assistant_id = 64fea0d3-2605-4c4c-be67-62258ebfa7a9
-- РФ = 47, Абхазия = 46.
--
-- Эффект: заявки (Telegram/CRM/email) по РФ/Абхазии НЕ доводятся до менеджеров и
-- НЕ приходят в ТГ-группу. Диалог остаётся в системе. Для всех остальных
-- ассистентов поведение не меняется (ключ lead_filter есть только у Anytour).
--
-- ПОРОГ ЦЕНЫ НЕ ВКЛЮЧАЕМ: по решению Павла (21.06) дешёвые туры не режем на
-- нашей стороне — вместо этого позже передаём ценность конверсии в Метрику и
-- он переводит РК на ДРР. Код поддерживает порог (ключ min_tour_price), но мы
-- его здесь сознательно НЕ задаём.
--
-- ⚠️ НЕ запускать без согласования. Сначала шаг 1 (бэкап), сохранить вывод.
-- ────────────────────────────────────────────────────────────────────────────

-- 1) БЭКАП текущего widget_config (сохранить результат перед изменением):
SELECT id, name, widget_config
FROM assistants
WHERE id = '64fea0d3-2605-4c4c-be67-62258ebfa7a9';

-- 2) ВКЛЮЧИТЬ фильтр (merge ключа lead_filter; касты делают SQL безопасным
--    как для json, так и для jsonb колонки):
UPDATE assistants
SET widget_config = (
        COALESCE(widget_config::jsonb, '{}'::jsonb)
        || jsonb_build_object(
               'lead_filter',
               jsonb_build_object(
                   'block_countries', jsonb_build_array(47, 46)
               )
           )
    )::json
WHERE id = '64fea0d3-2605-4c4c-be67-62258ebfa7a9';

-- 3) ПРОВЕРКА (должно показать {"block_countries":[47,46]}):
SELECT id, widget_config -> 'lead_filter' AS lead_filter
FROM assistants
WHERE id = '64fea0d3-2605-4c4c-be67-62258ebfa7a9';

